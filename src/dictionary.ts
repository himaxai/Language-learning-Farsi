export function getOfflineTranslation(word: string): string {
  const cleaned = word.replace(/[.,!?;:()"]/g, '').trim().toLowerCase();
  
  if (DICTIONARY[cleaned]) return DICTIONARY[cleaned];
  if (cleaned.endsWith('s') && DICTIONARY[cleaned.slice(0, -1)]) return DICTIONARY[cleaned.slice(0, -1)];
  if (cleaned.endsWith('es') && DICTIONARY[cleaned.slice(0, -2)]) return DICTIONARY[cleaned.slice(0, -2)];
  if (cleaned.endsWith('d') && DICTIONARY[cleaned.slice(0, -1)]) return DICTIONARY[cleaned.slice(0, -1)];
  if (cleaned.endsWith('ed') && DICTIONARY[cleaned.slice(0, -2)]) return DICTIONARY[cleaned.slice(0, -2)];
  if (cleaned.endsWith('ing') && DICTIONARY[cleaned.slice(0, -3)]) return DICTIONARY[cleaned.slice(0, -3)];
  if (cleaned.endsWith('ing') && DICTIONARY[cleaned.slice(0, -3) + 'e']) return DICTIONARY[cleaned.slice(0, -3) + 'e'];

  return cleaned;
}
